# 상담하는 데 필요한 기간은 1일보다 클 수 있기 때문에, 모든 상담을 할 수는 없습니다. 예를 들어서 1일에 상담을 하게 되면, 2,3일에 있는 상담은 할 수 없습니다. 2일에 있는 상담을 하게되면, 3,4,5,6일에 잡혀있는 상담은 할 수 없습니다.<br>
# 또한 N+1일째에는 회사에 없기 때문에, 6,7일에 있는 상담을 할 수 없습니다.<br>
