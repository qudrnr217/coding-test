n=int(input())
arr=[[3,10],[5 ,20],[1,10],[1,20],[2,15],[4,40],[2,200]]
time=0
d=[0]*(n+1)
sum=0
for i in range(n-1,-1,-1):
    time=arr[i][0]+i
    #시간안에 상담가능할 때
    if time<=n:
        d[i]=max(arr[i][1]+d[time],sum)
    #시간이 초과 될 때
    else:
        d[i]=sum
        

print(max(d))
        


